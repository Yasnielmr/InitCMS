package com.cms.init.model;

public class Grupo {
	
	private Long IdGrupo;
	private String Nombre;
	
	public Long getIdGrupo() {
		return IdGrupo;
	}
	public void setIdGrupo(Long idGrupo) {
		IdGrupo = idGrupo;
	}
	public String getNombre() {
		return Nombre;
	}
	public void setNombre(String nombre) {
		Nombre = nombre;
	}
	
	

}
